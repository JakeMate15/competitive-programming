void sol(){
    int n;
    cin >> n;

    vector<int> a(n), b(n);
    int mnA, mnB;
    mnA = mnB = INT_MAX;

    for(auto &x: a) {
        cin >> x;
        mnA = min(mnA,x);
    }

    for(auto &x: b) {
        cin >> x;
        mnB = min(mnB, x);
    }

    ll curr = 0;
    for(auto x: a) {
        curr += x + mnB;
    }

    ll curr2 = 0;
    for(auto x: b) {
        curr2 += x + mnA;
    }

    debln(min(curr, curr2));
}

